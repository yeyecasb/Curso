#Conversión de unidades: Escribe un programa que convierta la temperatura de grados
# Celsius a Fahrenheit. Solicita al usuario la temperatura en grados Celsius, utiliza
# operadores para realizar la conversión y la función print para mostrar el resultado.
celsius=int(input("ingrese temperatura en °C: "))
fahrenheit=(celsius*9/5)+32
print("El resultado es: ", fahrenheit , "°F")