#Crea dos variables numéricas y realiza operaciones aritméticas con ellas. Utiliza la
# función print para mostrar el resultado y la función type para mostrar el tipo de dato de
# cada variable.
variable_1=int(input("Ingrese el primer numero: "))
variable_2=int(input("Ingrese el segundo numero: "))
suma=variable_1+variable_2
resta=variable_1-variable_2
multiplicacion=variable_1*variable_2
division=variable_1/variable_2
print("La suma es ",suma," y su tipo de valor es ",type(suma))
print("La resta es ",resta," y su tipo de valor es ",type(resta))
print("La multiplicacion es ",multiplicacion," y su tipo de valor es ",type(multiplicacion))
print("La division es ",division," y su tipo de valor es ",type(division))