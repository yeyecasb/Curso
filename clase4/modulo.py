# La operacion MODULO (o resto) cuyo simbolo es el %
# sirve para obtener el resto de la division entre 2 numeros.

dividendo = 17
divisor = 5

normal = dividendo / divisor
cociente = dividendo // divisor
resto = dividendo % divisor

print('ejecicion normal : ', normal)
print('cociente: ', cociente)
print('resto: ', resto)
